def get_coastline_pixels(width: int, height: int):
    """
    Convert coastline coordinates to pixel positions for given image dimensions.
    Returns list of line segments as numpy arrays.
    """
    import numpy as np
    
    segments = []
    
    for coast in COASTLINES:
        points = []
        for lon, lat in coast:
            # Convert to pixel coordinates
            x = ((lon + 180) / 360.0) * width
            y = ((lat + 90) / 180.0) * height
            points.append([x, y])
        
        if len(points) > 1:
            segments.append(np.array(points, dtype=np.float32))
    
    return segments
