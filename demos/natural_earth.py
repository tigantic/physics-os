def get_coastlines_pixels(width: int, height: int, 
                          lon_range: Tuple[float, float] = (0, 360),
                          lat_range: Tuple[float, float] = (-90, 90)) -> List[np.ndarray]:
    """
    Convert Natural Earth coastlines to pixel coordinates.
    
    Args:
        width: Output image width in pixels
        height: Output image height in pixels
        lon_range: (min_lon, max_lon) - defaults to (0, 360)
        lat_range: (min_lat, max_lat) - defaults to (-90, 90)
        
    Returns:
        List of numpy arrays, each containing [x, y] pixel coordinates
    """
    lon_min, lon_max = lon_range
    lat_min, lat_max = lat_range
    
    segments = []
    
    for coastline in COASTLINES_110M:
        points = []
        for lon, lat in coastline:
            # Normalize longitude to 0-360 range if needed
            if lon < lon_min:
                lon += 360
            elif lon > lon_max:
                lon -= 360
            
            # Skip points outside range
            if not (lon_min <= lon <= lon_max and lat_min <= lat <= lat_max):
                continue
            
            # Convert to pixel coordinates
            x = ((lon - lon_min) / (lon_max - lon_min)) * width
            y = ((lat - lat_min) / (lat_max - lat_min)) * height
            
            points.append([x, y])
        
        if len(points) > 1:
            segments.append(np.array(points, dtype=np.float32))
    
    return segments


def get_coastlines_geo() -> List[np.ndarray]:
    """
    Get coastlines in geographic coordinates (lon, lat).
    
    Returns:
        List of numpy arrays with [lon, lat] coordinates
    """
    segments = []
    for coastline in COASTLINES_110M:
        points = np.array(coastline, dtype=np.float32)
        if len(points) > 1:
            segments.append(points)
    return segments


# Backward compatibility
def get_coastline_pixels(width: int, height: int) -> List[np.ndarray]:
    """Backward-compatible function name."""
    return get_coastlines_pixels(width, height)
