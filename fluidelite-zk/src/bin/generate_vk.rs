//! Binary: `generate_vk`
//!
//! Runs the Groth16 trusted setup for `SimpleMembershipCircuit`, extracts the
//! `VerifyingKey`, and emits Solidity constant declarations suitable for
//! embedding in an on-chain Groth16 verifier.
//!
//! Usage:
//!   cargo run --bin generate-vk --features groth16
//!
//! Output:
//!   Prints Solidity `uint256 internal constant ...` lines to stdout.
//!   Also writes `vk.json` to the working directory for programmatic use.

#[cfg(feature = "groth16")]
fn main() {
    use ark_bn254::{Bn254, Fq, Fr, G1Affine, G2Affine};
    use ark_ff::{BigInteger, PrimeField};
    use ark_groth16::Groth16;
    use ark_snark::SNARK;
    use ark_std::rand::{rngs::StdRng, SeedableRng};
    use std::collections::BTreeMap;
    use std::io::Write;

    // Import the circuit from the groth16_prover module.
    use fluidelite_zk::groth16_prover::SimpleMembershipCircuit;

    eprintln!("╔═══════════════════════════════════════════════════════════╗");
    eprintln!("║        Groth16 Trusted Setup — VK Generator              ║");
    eprintln!("╚═══════════════════════════════════════════════════════════╝");

    // ── Deterministic RNG for reproducible VK + test fixtures ────
    // Fixed seed ensures `vk.json` and Solidity constants are identical
    // across runs, enabling committed Foundry test fixtures.
    const DETERMINISTIC_SEED: u64 = 0x4859_5045_5254_454E; // "HYPERTEN" in ASCII
    let mut rng = StdRng::seed_from_u64(DETERMINISTIC_SEED);

    // ── Trusted setup ──────────────────────────────────────────────
    let circuit = SimpleMembershipCircuit::new();
    let (pk, vk) = Groth16::<Bn254>::circuit_specific_setup(circuit, &mut rng)
        .expect("Trusted setup failed");

    // Quick sanity: generate a proof and verify with the VK.
    let secret = Fr::from(42u64);
    let hash = secret * secret; // SimpleMembershipCircuit constraint
    let circuit = SimpleMembershipCircuit {
        secret: Some(secret),
        hash: Some(hash),
    };
    let proof = Groth16::<Bn254>::prove(&pk, circuit, &mut rng)
        .expect("Proof generation failed");
    let valid = Groth16::<Bn254>::verify(&vk, &[hash], &proof)
        .expect("Verification failed");
    assert!(valid, "Self-test: proof must verify against generated VK");
    eprintln!("✓ Self-test passed: proof verified against VK");

    // ── Helper: Fq → 0x-prefixed 32-byte big-endian hex ─────────
    fn fq_hex(f: &Fq) -> String {
        let bigint = f.into_bigint();
        let mut bytes = bigint.to_bytes_le();
        bytes.reverse(); // LE → BE
        format!("0x{}", hex::encode(bytes))
    }

    fn fr_hex(f: &Fr) -> String {
        let bigint = f.into_bigint();
        let mut bytes = bigint.to_bytes_le();
        bytes.reverse();
        format!("0x{}", hex::encode(bytes))
    }

    // ── Extract VK points ────────────────────────────────────────
    let alpha: &G1Affine = &vk.alpha_g1;
    let beta: &G2Affine = &vk.beta_g2;
    let gamma: &G2Affine = &vk.gamma_g2;
    let delta: &G2Affine = &vk.delta_g2;

    // G2 point coordinates: x = c0 + c1*u (Fp2), so:
    //   x_re = c0, x_im = c1
    // EVM ecPairing expects: (x_im, x_re, y_im, y_re) per the existing contract convention.
    // Our Solidity uses: X1 = x_im = c1, X2 = x_re = c0, Y1 = y_im = c1, Y2 = y_re = c0

    println!("// ═══════════════════════════════════════════════════════════");
    println!("// VERIFICATION KEY — auto-generated by `cargo run --bin generate-vk`");
    println!("// Circuit: SimpleMembershipCircuit (secret² = hash)");
    println!("// Curve: BN254 (alt_bn128)");
    println!("// IC count: {} (1 constant + {} public inputs)", vk.gamma_abc_g1.len(), vk.gamma_abc_g1.len() - 1);
    println!("// ═══════════════════════════════════════════════════════════");
    println!();

    // α ∈ G1
    println!("// α ∈ G1");
    println!("uint256 internal constant ALPHA_X = {};", fq_hex(&alpha.x));
    println!("uint256 internal constant ALPHA_Y = {};", fq_hex(&alpha.y));
    println!();

    // Macro for G2 points
    fn print_g2(name: &str, p: &G2Affine) {
        println!("// {} ∈ G2", name);
        // X1 = x.c1 (imaginary), X2 = x.c0 (real)
        println!("uint256 internal constant {}_X1 = {};", name, fq_hex(&p.x.c1));
        println!("uint256 internal constant {}_X2 = {};", name, fq_hex(&p.x.c0));
        // Y1 = y.c1 (imaginary), Y2 = y.c0 (real)
        println!("uint256 internal constant {}_Y1 = {};", name, fq_hex(&p.y.c1));
        println!("uint256 internal constant {}_Y2 = {};", name, fq_hex(&p.y.c0));
        println!();
    }

    print_g2("BETA", beta);
    print_g2("GAMMA", gamma);
    print_g2("DELTA", delta);

    // IC points (G1)
    println!("// IC points (γ⁻¹·(β·Aᵢ + α·Bᵢ + Cᵢ)) for {} public inputs", vk.gamma_abc_g1.len() - 1);
    for (i, ic) in vk.gamma_abc_g1.iter().enumerate() {
        println!("uint256 internal constant IC{}_X = {};", i, fq_hex(&ic.x));
        println!("uint256 internal constant IC{}_Y = {};", i, fq_hex(&ic.y));
        println!();
    }

    // ── Write vk.json ────────────────────────────────────────────
    let mut json_map = BTreeMap::new();
    json_map.insert("alpha_x".to_string(), fq_hex(&alpha.x));
    json_map.insert("alpha_y".to_string(), fq_hex(&alpha.y));
    json_map.insert("beta_x1".to_string(), fq_hex(&beta.x.c1));
    json_map.insert("beta_x2".to_string(), fq_hex(&beta.x.c0));
    json_map.insert("beta_y1".to_string(), fq_hex(&beta.y.c1));
    json_map.insert("beta_y2".to_string(), fq_hex(&beta.y.c0));
    json_map.insert("gamma_x1".to_string(), fq_hex(&gamma.x.c1));
    json_map.insert("gamma_x2".to_string(), fq_hex(&gamma.x.c0));
    json_map.insert("gamma_y1".to_string(), fq_hex(&gamma.y.c1));
    json_map.insert("gamma_y2".to_string(), fq_hex(&gamma.y.c0));
    json_map.insert("delta_x1".to_string(), fq_hex(&delta.x.c1));
    json_map.insert("delta_x2".to_string(), fq_hex(&delta.x.c0));
    json_map.insert("delta_y1".to_string(), fq_hex(&delta.y.c1));
    json_map.insert("delta_y2".to_string(), fq_hex(&delta.y.c0));

    for (i, ic) in vk.gamma_abc_g1.iter().enumerate() {
        json_map.insert(format!("ic{}_x", i), fq_hex(&ic.x));
        json_map.insert(format!("ic{}_y", i), fq_hex(&ic.y));
    }
    json_map.insert("num_public_inputs".to_string(), (vk.gamma_abc_g1.len() - 1).to_string());

    // Also export a test proof + public inputs for Foundry test seeds.
    let proof_secret = Fr::from(7u64);
    let proof_hash = proof_secret * proof_secret;
    let test_circuit = SimpleMembershipCircuit {
        secret: Some(proof_secret),
        hash: Some(proof_hash),
    };
    let test_proof = Groth16::<Bn254>::prove(&pk, test_circuit, &mut rng)
        .expect("Test proof failed");
    let test_valid = Groth16::<Bn254>::verify(&vk, &[proof_hash], &test_proof)
        .expect("Test verify failed");
    assert!(test_valid, "Self-test proof did not verify");

    // Serialize proof points for Solidity test: [A.x, A.y, B.x1, B.x0, B.y1, B.y0, C.x, C.y]
    json_map.insert("test_proof_a_x".to_string(), fq_hex(&test_proof.a.x));
    json_map.insert("test_proof_a_y".to_string(), fq_hex(&test_proof.a.y));
    json_map.insert("test_proof_b_x1".to_string(), fq_hex(&test_proof.b.x.c1));
    json_map.insert("test_proof_b_x0".to_string(), fq_hex(&test_proof.b.x.c0));
    json_map.insert("test_proof_b_y1".to_string(), fq_hex(&test_proof.b.y.c1));
    json_map.insert("test_proof_b_y0".to_string(), fq_hex(&test_proof.b.y.c0));
    json_map.insert("test_proof_c_x".to_string(), fq_hex(&test_proof.c.x));
    json_map.insert("test_proof_c_y".to_string(), fq_hex(&test_proof.c.y));
    json_map.insert("test_public_input".to_string(), fr_hex(&proof_hash));

    let json = serde_json::to_string_pretty(&json_map).expect("JSON serialize");
    let vk_path = "vk.json";
    std::fs::write(vk_path, &json).expect("Failed to write vk.json");
    eprintln!("✓ Wrote {vk_path} ({} bytes)", json.len());

    eprintln!();
    eprintln!("Copy the Solidity constants above into your verifier contract.");
    eprintln!("Use vk.json for programmatic access or Foundry test fixtures.");
}

#[cfg(not(feature = "groth16"))]
fn main() {
    eprintln!("Error: This binary requires the `groth16` feature.");
    eprintln!("Run with: cargo run --bin generate-vk --features groth16");
    std::process::exit(1);
}
