    for result in &results {
        eprintln!("в•‘ {:60} в•‘", result.config.description);
        eprintln!("в•‘   Throughput: {:8.2} frames/sec | {:6.4} GB/s                    в•‘",
                 result.throughput.frames_per_sec, result.throughput.gbps);
        eprintln!("в•‘   Latency:    p50={:6.0}Ојs  p99={:6.0}Ојs                            в•‘",
                 result.e2e_latency.p50_us, result.e2e_latency.p99_us);
        eprintln!("в•‘   Compression: {:6.1}x  Memory saved: {:8} bytes               в•‘",
                 result.memory.compression_ratio, result.memory.memory_saved_bytes);
        eprintln!("в• в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ј");
    }

    eprintln!("в•ҡв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•қ");
