@dataclass
class EUVPhoton:
    """EUV photon properties."""
    wavelength_nm: float = 13.5       # EUV wavelength
    energy_eV: float = 91.8           # Photon energy (hc/λ)
    
    @property
    def energy_J(self) -> float:
        return self.energy_eV * EV_TO_J


@dataclass
class SecondaryElectron:
    """
    Secondary electron generated by EUV absorption.
    
    These electrons cause the "stochastic blur" by traveling
    beyond the intended exposure region.
    """
    position: np.ndarray              # [x, y, z] in nm
    velocity: np.ndarray              # [vx, vy, vz] in m/s
    energy_eV: float                  # Kinetic energy
    trapped: bool = False
    distance_traveled_nm: float = 0.0
    
    @classmethod
    def generate(cls, origin: np.ndarray, photon_energy_eV: float) -> 'SecondaryElectron':
        """Generate secondary electron with random direction and energy."""
        # Energy distribution (typical 1-30 eV for secondaries)
        # Most secondaries have low energy (peaked at ~5 eV)
        energy_eV = np.random.exponential(5.0)
        energy_eV = min(energy_eV, photon_energy_eV - 10)  # Conservation
        energy_eV = max(energy_eV, 1.0)
        
        # Random direction (isotropic)
        theta = np.random.uniform(0, np.pi)
        phi = np.random.uniform(0, 2*np.pi)
        
        # Velocity from kinetic energy: E = 0.5 * m * v²
        speed = np.sqrt(2 * energy_eV * EV_TO_J / M_ELECTRON)
        
        velocity = speed * np.array([
            np.sin(theta) * np.cos(phi),
            np.sin(theta) * np.sin(phi),
            np.cos(theta)
        ])
        
        return cls(
            position=origin.copy(),
            velocity=velocity,
            energy_eV=energy_eV
        )


