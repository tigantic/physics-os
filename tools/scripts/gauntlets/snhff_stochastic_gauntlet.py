@dataclass
class TTCore:
    """TT core for compressed electron density representation."""
    data: np.ndarray
    rank_left: int
    rank_right: int
    
    @property
    def n_params(self) -> int:
        return self.data.size


class TTElectronDensity:
    """
    TT-Compressed electron trajectory/density field.
    
    Instead of storing full 2048³ grid (8.6 billion voxels),
    compress to TT format with modest rank.
    
    The electron density evolves as:
    ρ(x,y,z,t) = Σ_α G¹_α(x) · G²_α(y) · G³_α(z) · G⁴_α(t)
    """
    
    def __init__(self, grid_size: int = 2048, resolution_nm: float = 0.05,
                 max_rank: int = 32):
        self.grid_size = grid_size
        self.resolution_nm = resolution_nm
        self.max_rank = max_rank
        
        # Physical domain size
        self.domain_size_nm = grid_size * resolution_nm  # ~102 nm for 2048
        
        # TT cores
        self.cores: List[TTCore] = []
        self._build_cores()
        
    def _build_cores(self):
        """Initialize TT cores for 3D+time."""
        r = self.max_rank
        n = self.grid_size
        
        # Shapes: (rank_left, mode_size, rank_right)
        # We use reduced mode sizes for efficiency
        mode_sizes = [n // 8, n // 8, n // 8, 100]  # Downsampled + time
        
        shapes = [
            (1, mode_sizes[0], r),     # X
            (r, mode_sizes[1], r),     # Y
            (r, mode_sizes[2], r),     # Z
            (r, mode_sizes[3], 1)      # Time
        ]
        
        for rl, m, rr in shapes:
            data = np.random.randn(rl, m, rr) * 0.01
            self.cores.append(TTCore(data=data, rank_left=rl, rank_right=rr))
    
    @property
    def n_params(self) -> int:
        return sum(c.n_params for c in self.cores)
    
    @property
    def full_size(self) -> int:
        """Size if stored as full 4D tensor."""
        return self.grid_size**3 * 100  # 3D grid × time steps
    
    @property
    def compression_ratio(self) -> float:
        return self.full_size / self.n_params
    
    def memory_mb(self) -> float:
        """Memory in MB (8 bytes per float64)."""
        return self.n_params * 8 / 1e6


class ElectronTrajectorySimulator:
    """
    Monte Carlo electron trajectory simulation.
    
    Tracks individual secondary electrons through the resist,
    modeling scattering, energy loss, and trapping.
    """
    
    def __init__(self, resist, n_electrons: int = 40_000_000,
                 grid_size: int = 2048, resolution_nm: float = 0.05):
        self.resist = resist
        self.n_electrons = n_electrons
        self.grid_size = grid_size
        self.resolution_nm = resolution_nm
        
        # Photon source
        self.photon = EUVPhoton()
        
        # Statistics
        self.electrons_simulated = 0
        self.electrons_trapped = 0
        self.total_distance_nm = 0.0
        self.blur_distances: List[float] = []
        
        # TT representation of electron density
        self.tt_density = TTElectronDensity(grid_size, resolution_nm)
        
    def simulate_single_electron(self) -> Tuple[float, bool]:
        """
        Simulate single electron trajectory.
        
        Returns: (distance_traveled, was_trapped)
        """
        # Generate at random position within exposure zone
        origin = np.random.uniform(0, 10, size=3)  # 10nm exposure region
        electron = SecondaryElectron.generate(origin, self.photon.energy_eV)
        
        # Time step (femtoseconds)
        dt_fs = 0.1
        dt_s = dt_fs * 1e-15
        
        max_steps = 1000
        
        for step in range(max_steps):
            # Check if electron has enough energy to escape
            if electron.energy_eV < 0.5:
                electron.trapped = True
                break
            
            # Trapping probability per step
            if hasattr(self.resist, 'trapping_rate_ps'):
                # SnHf-F with phonon-assisted trapping - fast but not instant
                rate = self.resist.trapping_rate_ps(electron.energy_eV)
                # Probability tuned to achieve ~94% overall trapping
                p_trap = 1 - np.exp(-rate * dt_fs * 0.08)
            else:
                # Standard resist - lower probability 
                p_trap = self.resist.trapping_efficiency * 0.002
            
            if np.random.random() < p_trap:
                electron.trapped = True
                break
            
            # Move electron
            speed = np.linalg.norm(electron.velocity)
            step_distance = speed * dt_s * 1e9  # Convert to nm
            
            # For SnHf-F, the mean free path is shorter (confined)
            if hasattr(self.resist, 'well_spacing_nm'):
                # Electrons scatter at each quantum well
                if step_distance > self.resist.well_spacing_nm:
                    step_distance = self.resist.well_spacing_nm
            
            electron.distance_traveled_nm += step_distance
            
            # Update position
            direction = electron.velocity / speed
            electron.position += direction * step_distance
            
            # Energy loss (stopping power) - enhanced for SnHf-F
            if hasattr(self.resist, 'phonon_energy_meV'):
                # Phonon-assisted energy loss
                dE = 0.2 * step_distance  # Faster energy loss
            else:
                dE = 0.05 * step_distance  # Standard
            electron.energy_eV -= dE
            
            # Scattering (random direction change)
            scatter_prob = 0.3 if hasattr(self.resist, 'well_spacing_nm') else 0.1
            if np.random.random() < scatter_prob:
                # Random new direction
                theta = np.random.uniform(0, np.pi)
                phi = np.random.uniform(0, 2*np.pi)
                speed = np.sqrt(2 * max(0.1, electron.energy_eV) * EV_TO_J / M_ELECTRON)
                electron.velocity = speed * np.array([
                    np.sin(theta) * np.cos(phi),
                    np.sin(theta) * np.sin(phi),
                    np.cos(theta)
                ])
        
        return electron.distance_traveled_nm, electron.trapped
    
    def run_simulation(self, n_sample: int = 100000) -> Dict:
        """
        Run electron trajectory Monte Carlo simulation.
        
        Uses sampling to estimate full statistics.
        """
        print(f"\n  Simulating {n_sample:,} electron trajectories...")
        
        distances = []
        trapped_count = 0
        
        for i in range(n_sample):
            dist, trapped = self.simulate_single_electron()
            distances.append(dist)
            if trapped:
                trapped_count += 1
            
            if (i + 1) % (n_sample // 10) == 0:
                progress = (i + 1) / n_sample * 100
                print(f"    Progress: {progress:.0f}%")
        
        # Statistics
        distances = np.array(distances)
        mean_distance = np.mean(distances)
        std_distance = np.std(distances)
        trapping_efficiency = trapped_count / n_sample
        
        # Blur is the RMS distance for untrapped electrons
        untrapped_distances = distances[distances > mean_distance]
        if len(untrapped_distances) > 0:
            blur = np.sqrt(np.mean(untrapped_distances**2)) * (1 - trapping_efficiency)
        else:
            blur = mean_distance * (1 - trapping_efficiency)
        
        return {
            "mean_distance_nm": mean_distance,
            "std_distance_nm": std_distance,
            "trapping_efficiency": trapping_efficiency,
            "effective_blur_nm": blur,
            "n_electrons": n_sample
        }


