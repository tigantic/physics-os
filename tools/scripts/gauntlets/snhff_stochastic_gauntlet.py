# LINE EDGE ROUGHNESS (LER) SIMULATION
# =============================================================================

class LERSimulator:
    """
    Line Edge Roughness simulation.
    
    LER arises from:
    1. Shot noise (photon statistics)
    2. Secondary electron blur
    3. Development kinetics
    
    The SnHf-F quantum well reduces components 2 and 3.
    """
    
    def __init__(self, resist, feature_size_nm: float = 10.0,
                 dose_mJ_cm2: float = 30.0):
        self.resist = resist
        self.feature_size_nm = feature_size_nm
        self.dose_mJ_cm2 = dose_mJ_cm2
        
        # EUV photon flux at given dose
        photon_energy_J = 91.8 * EV_TO_J
        self.photons_per_nm2 = (dose_mJ_cm2 * 1e-3 * 1e14) / photon_energy_J
        
    def compute_ler(self, blur_nm: float) -> Tuple[float, Dict]:
        """
        Compute Line Edge Roughness.
        
        LER (3σ) = √(LER_shot² + LER_blur² + LER_base²)
        """
        # Shot noise contribution (Poisson statistics)
        # LER_shot ∝ 1/√(photons in feature edge)
        edge_width_nm = 2.0  # Edge transition region
        photons_in_edge = self.photons_per_nm2 * self.feature_size_nm * edge_width_nm
        ler_shot = 1.0 / np.sqrt(photons_in_edge) * 10  # Scaling factor
        
        # Blur contribution
        ler_blur = blur_nm * 0.8  # Blur directly affects edge definition
        
        # Base resist contribution (material uniformity, development)
        ler_base = self.resist.base_ler_nm * 0.5
        
        # Total LER (quadrature sum)
        ler_total = np.sqrt(ler_shot**2 + ler_blur**2 + ler_base**2)
        
        components = {
            "ler_shot_nm": ler_shot,
            "ler_blur_nm": ler_blur,
            "ler_base_nm": ler_base,
            "ler_total_3sigma_nm": ler_total
        }
        
        return ler_total, components


