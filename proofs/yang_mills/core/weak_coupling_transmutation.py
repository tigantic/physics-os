    strong = strong_coupling_gap(g)
    weak = weak_coupling_gap(g, L)
    
    # Crossover at g ~ 1
    alpha = np.tanh((g - 1) * 2)  # Smooth step
    
    # Interpolate
    return (1 + alpha)/2 * strong + (1 - alpha)/2 * weak


